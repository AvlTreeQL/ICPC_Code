#include<stdio.h>
int main()
{ 
	int m,a[10],i,t;
	scanf("%d",&m);
	for(i=0;m!=0;i++)
	{
		a[i]=m%10;
		m=m/10;	
	}
	printf("%d\n",i);
	t=i;
	for(i=t-1;i>=0;i--)
			printf("%d ",a[i]);
	printf("\n");
	for(i=0;i<t;i++)
			printf("%d ",a[i]);
	return 0;
}
