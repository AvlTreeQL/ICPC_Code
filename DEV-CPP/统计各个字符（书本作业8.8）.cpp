#include<stdio.h>
#include<string.h>
int main()
{
	int n1,n2,n3,n4,n5;
	n1=n2=n3=n4=n5=0;
	char a[80],*p=a;
	gets(a);
	while(*(p++)!='\0')
	{
		if(*(p-1)>='A'&&*(p-1)<='Z')
			n1++;
		else if(*(p-1)>='a'&&*(p-1)<='z')
			n2++;
		else if(*(p-1)==' ')
			n3++;
		else if(*(p-1)>='0'&&*(p-1)<='9')
			n4++;
		else n5++;
	}
	printf("%d %d %d %d %d\n",n1,n2,n3,n4,n5);
	return 0;
}
/*
问题描述
输入一行文字，找出其中大写字母、小写字母、空格、数字以及其他字符各有多少。用指针方法实现。
输入形式
输入一行，输入一字符串（字符串的字符不多于80个）。
输出形式
输出一行，出其中大写字母、小写字母、空格、数字以及其他字符各有多少个。各个数值之间以空格分隔。
样例输入
It is a dog. 2 or 3 years old?
样例输出
1 17 8 2 
*/